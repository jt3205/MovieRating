package rank;

public class RankVO {
	private int rank;
	private String title;
	private String date;
	
	public int getRank() {
		return rank;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
